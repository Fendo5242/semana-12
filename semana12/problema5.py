def suma(a):
    total = 0
    for i in range(len(a)):
        total += a[i] 
    return total
funcion1=[1,2,3,4]
print(suma(funcion1))
def multiplicacion(a):
    total = 1
    for i in range(len(a)):
        total *= a[i]
    return total
funcion2=[1,2,3,4]
print(multiplicacion(funcion2))

